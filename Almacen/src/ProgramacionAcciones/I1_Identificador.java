package ProgramacionAcciones;

public class I1_Identificador {

    public int numeroBuscador(int Index) {
        int Id = 0;
        
        if(Index == 0){
            Id = 1;
        }
        if(Index == 1){
            Id = 2;
        }
        if(Index == 2){
            Id = 3;
        }
        if(Index == 3){
            Id = 4;
        }
        if(Index == 4){
            Id = 5;
        }
        if(Index == 5){
            Id = 6;
        }
        if(Index == 6){
            Id = 7;
        }
        if(Index == 7){
            Id = 8;
        }
        return Id;  
    }
}
